preco1 = float(input('Preço do Primeiro Produto: '))
preco2 = float(input('Preço do Segundo produto: '))
média = (preco1  + preco2) / 2

print('Pagando {:.1f} reais pelo primeiro produto e {:.1f} reais pelo segundo produto, o preço médio de venda é {:.1f} reais'.format(preco1, preco2, média))

